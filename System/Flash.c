#include "flash.h"


////FLASH写入数据
void FLASH_W(u32 add,u16 dat,u16 dat2,u16 dat3,u16 dat4,u16 dat5,u16 dat6)	//参数1：32位FLASH地址。参数2：16位数据
{ 
//	 RCC_HSICmd(ENABLE); //打开HSI时钟
	 FLASH_Unlock();  //解锁FLASH编程擦除控制器
     FLASH_ClearFlag(FLASH_FLAG_BSY|FLASH_FLAG_EOP|FLASH_FLAG_PGERR|FLASH_FLAG_WRPRTERR);//清除标志位
     FLASH_ErasePage(add);     //擦除指定地址页
     FLASH_ProgramHalfWord(add,dat); //从指定页的addr地址开始写
	 FLASH_ProgramHalfWord(add+2,dat2); //从指定页的addr地址+2开始写
	 FLASH_ProgramHalfWord(add+4,dat3); //从指定页的addr地址+4开始写
	 FLASH_ProgramHalfWord(add+6,dat4); //从指定页的addr地址+6开始写
	 FLASH_ProgramHalfWord(add+8,dat5); //从指定页的addr地址+8开始写
	 FLASH_ProgramHalfWord(add+10,dat6); //从指定页的addr地址+10开始写
     FLASH_ClearFlag(FLASH_FLAG_BSY|FLASH_FLAG_EOP|FLASH_FLAG_PGERR|FLASH_FLAG_WRPRTERR);//清除标志位
     FLASH_Lock();    //锁定FLASH编程擦除控制器
}

//FLASH读出数据
u16 FLASH_R(u32 add)	 //参数1：32位读出FLASH地址。返回值：16位数据
{
	u16 a;
    a = *(u16*)(add);//从指定页的addr地址开始读
return a;
}

